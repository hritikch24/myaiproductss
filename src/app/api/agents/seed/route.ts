export async function POST(request: Request) {
  try {
    const { action } = await request.json();

    if (action === "migrate") {
      const migrationSQL = readFileSync(
        join(process.cwd(), "migrations", "011_agents_tables.sql"),
        "utf-8"
      );
      await pool.query(migrationSQL);
      return NextResponse.json({ success: true, message: "Migration completed" });
    }

    if (action === "seed") {
      // Check if agents already exist
      const existing = await pool.query("SELECT COUNT(*) FROM agents");
      if (parseInt(existing.rows[0].count) > 0) {
        return NextResponse.json({
          success: false,
          message: `Database already has ${existing.rows[0].count} agents. Delete them first or use action: "reset"`,
        });
      }

      let inserted = 0;
      for (const agent of AGENTS_SEED_DATA) {
        await pool.query(
          `INSERT INTO agents (name, slug, category, description, system_prompt, personality_tone, icon, active)
           VALUES ($1, $2, $3, $4, $5, $6, $7, true)
           ON CONFLICT (slug) DO NOTHING`,
          [
            agent.name,
            agent.slug,
            agent.category,
            agent.description,
            agent.system_prompt,
            agent.personality_tone,
            agent.icon,
          ]
        );
        inserted++;
      }

      return NextResponse.json({
        success: true,
        message: `Seeded ${inserted} agents across ${new Set(AGENTS_SEED_DATA.map((a) => a.category)).size} categories`,
      });
    }

    if (action === "reset") {
      await pool.query("DELETE FROM agent_messages");
      await pool.query("DELETE FROM agents");

      let inserted = 0;
      for (const agent of AGENTS_SEED_DATA) {
        await pool.query(
          `INSERT INTO agents (name, slug, category, description, system_prompt, personality_tone, icon, active)
           VALUES ($1, $2, $3, $4, $5, $6, $7, true)`,
          [
            agent.name,
            agent.slug,
            agent.category,
            agent.description,
            agent.system_prompt,
            agent.personality_tone,
            agent.icon,
          ]
        );
        inserted++;
      }

      return NextResponse.json({
        success: true,
        message: `Reset complete. Seeded ${inserted} agents.`,
      });
    }

    return NextResponse.json(
      { success: false, message: 'Invalid action. Use "migrate", "seed", or "reset".' },
      { status: 400 }
    );
  } catch (error) {
    console.error("Seed error:", error);
    return NextResponse.json(
      { success: false, error: String(error) },
      { status: 500 }
    );
  }
}
